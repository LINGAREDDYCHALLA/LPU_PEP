// Traffic Light: if else if and else
// trafficColor: user enter: yellow: Slow down
//  green: You can Go
// red: You must stop

let lightColor = prompt("Enter the traffic light color");
if (lightColor === "green") {
  console.log("You can Go");
} else if (lightColor === "yellow") {
  console.log("You can slow down");
} else {
  console.log("You can stop");
}

